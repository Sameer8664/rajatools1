<script>
  document.addEventListener('DOMContentLoaded', () => {
    // Select all category buttons (e.g., "Hand Tools", "Power Tools", "All Tools")
    const categoryButtons = document.querySelectorAll('.category-button');

    // Select all individual tool cards
    const toolCards = document.querySelectorAll('.tool-card');

    // Define the maximum number of image slots to display
    const maxImageSlots = 5;

    // --- Function to Filter and Display Cards ---
    // This function now encapsulates the logic for showing/hiding cards
    // and enforcing the 'maxImageSlots' limit.
    const filterAndDisplayCards = (selectedCategory) => {
      let visibleCardCount = 0; // Initialize a counter for cards currently visible

      toolCards.forEach(card => {
        const cardCategory = card.dataset.category; // Get the category of the current card

        // First, check if the card's category matches the selected filter
        const isCategoryMatch = (selectedCategory === 'all' || cardCategory === selectedCategory);

        if (isCategoryMatch) {
          // If the card belongs to the selected category,
          // check if we've already displayed the maximum allowed slots.
          if (visibleCardCount < maxImageSlots) {
            card.style.display = 'flex'; // Show the card (using 'flex' as per your CSS)
            visibleCardCount++; // Increment the counter for visible cards
          } else {
            // If the category matches but we've hit the limit, hide this card.
            card.style.display = 'none';
          }
        } else {
          // If the card's category doesn't match the selected filter, hide it.
          card.style.display = 'none';
        }
      });
    };

    // --- Attach Event Listeners to Category Buttons ---
    categoryButtons.forEach(button => {
      button.addEventListener('click', () => {
        // Remove 'active' class from all buttons to reset styling
        categoryButtons.forEach(btn => btn.classList.remove('active'));
        // Add 'active' class to the clicked button
        button.classList.add('active');

        // Get the data-category value from the clicked button
        const selectedCategory = button.dataset.category;

        // Call our new function to filter and display cards based on the selection
        filterAndDisplayCards(selectedCategory);
      });
    });

    // --- Initial Load: Show "All Tools" (limited to 5) ---
    // This ensures that when the page first loads, the "All Tools" category
    // is active and only its first 5 tools are displayed.
    filterAndDisplayCards('all');
  });
</script>